int	ft_atoi(char *str)
{
	int	i;
	int	m;
	int	re;

	i = 0;
	m = 1;
	re = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
			i++;
	if (str[i] == '-')
	{
		return (0);
	}
	if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		re = re * 10 + str[i] - '0';
		i++;
	}
	return (re * m);
}
