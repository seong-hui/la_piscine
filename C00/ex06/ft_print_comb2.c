#include <unistd.h>

void	ft_print_comb2(void)
{
	int		n1;
	int		n2;
	char	str1[2];
	char	str2[2];

	n1 = 0;
	while (n1 <= 98)
	{
		n2 = n1 + 1;
		while (n2 <= 99)
		{
			str1[0] = n1 / 10 + '0';
			str1[1] = n1 % 10 + '0';
			str2[0] = n2 / 10 + '0';
			str2[1] = n2 % 10 + '0';
			n2++;
			write(1, str1, 2);
			write(1, " ", 1);
			write(1, str2, 2);
			if (n1 == 98)
				break ;
			write(1, ", ", 2);
		}
		n1++;
	}
}
