#include <unistd.h>

void	ft_putnbr(int nb)
{
	char		c;
	long long	n;

	n = nb;
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	c = n % 10 + '0';
	write (1, &c, 1);
}
